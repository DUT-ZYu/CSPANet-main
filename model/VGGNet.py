class Decoder(nn.Module):
    def __init__(self, net) -> None:
        super(Decoder, self).__init__()
        dec_layers = list(net.children())
        self.dec_1 = nn.Sequential(*dec_layers[:4])  # input -> relu1_1
        self.dec_2 = nn.Sequential(*dec_layers[4:17])  # relu1_1 -> relu2_1
        self.dec_3 = nn.Sequential(*dec_layers[17:24])  # relu2_1 -> relu3_1
        self.dec_4 = nn.Sequential(*dec_layers[24:29])  # relu3_1 -> relu4_1
        self.sim_alpha = nn.Parameter(torch.ones(1), requires_grad=True)
        self.sim_beta = nn.Parameter(torch.ones(1), requires_grad=True)

    def forward(self, x, skip=None, fusion=True):  #DSC structure
        dec1 = self.dec_1(x)
        if fusion:
            dec1 = self.sim_alpha * dec1 + self.sim_beta * skip['cwct_1']  # 64
        dec2 = self.dec_2(dec1)
        if fusion:
            dec2 = self.sim_alpha * dec2 + self.sim_beta * skip['cwct_2']  # 128
        dec3 = self.dec_3(dec2)
        out = self.dec_4(dec3)
        return out